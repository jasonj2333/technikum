<?php

$kolor = "red";

switch ($kolor){
    case "red":
        echo "Dominującym kolororem jest czerwony";
        break;
    case "blue":
        echo "Dominującym kolororem jest niebieski";
        break;
    case "green":
        echo "Dominującym kolororem jest zielony";
        break;
    default:
        echo "Brak dominującego koloru";
}