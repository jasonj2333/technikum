a = 5
b = 17

if a == b:
    print("Wartości są równe")

if a != b:
    print("Wartości nie są sobie równe")
    
if a > b:
    print("Wartość a jest większa od b")

if a < b:
    print("Wartość a jest mniejsza od b")
    
if a >= b:
    print("Wartość a jest większa lub równa od b")
    
if a <= b:
    print("Wartość a jest mniejsza lub równa od b")